package com.kh.day01.basic;

public class Exam_DataType {
	public static void main(String [] args)
	{
		// 기본형
		// boolean (불린) 1byte
		// char (문자) 2byte
		// byte 1byte, short 2byte, int 4byte, long 8byte(정수) 
		// float 4byte, double 8byte (실수)
		
		// 참조형
		// String
		
		boolean result = true;
		
		char fChar = 'a';
		
		String sStr = "Hello Java";
		
		// 기본형 정수 part
		byte bNum = 127; // 최대값
		short sNum = 32767; // 최대값
		int iNum = 2147483647; // 최대값
		long lNum = 2147483648l; // 접미사 l 을 붙여줘야 함.
		
		// 기본형 실수 part
		float fNum = 23.0626f; // 접미사 f 를 붙여줘야 함.
		double dNum = 23.1026;
		
	}
}







